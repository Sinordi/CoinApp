import Foundation

protocol CoinManagerDelegate {
    func didUpdateCoinPrice(coinManager: CoinManager, coinPrice: CoinData)
    func didFailWithError(error: Error)
}

struct CoinManager {
    
    var delegate: CoinManagerDelegate?
    
    let baseURL = "https://rest.coinapi.io/v1/exchangerate/BTC"
    let apiKey = "BFE3BDE7-0B1C-4F00-8F86-7E08A392723A"
    let currencyArray = ["AUD", "BRL","CAD","CNY","EUR","GBP","HKD","IDR","ILS","INR","JPY","MXN","NOK","NZD","PLN","RON","RUB","SEK","SGD","USD","ZAR"]
    
    
    func getCoinPrice(for currency: String) {
        if let url = URL(string: "\(baseURL)/\(currency)?apikey=\(apiKey)") {
            let session = URLSession(configuration: .default)
            let task = session.dataTask(with: url) { data, response, error in
                if error != nil {
                    self.delegate?.didFailWithError(error: error!)
                    return
                }
                if let safeData = data {
                    if let coinPrice = parseJSON(safeData) {
                        self.delegate?.didUpdateCoinPrice(coinManager: self, coinPrice: coinPrice)
                    }
                }
            }
            task.resume()
        }
    
        
    }
    
    func parseJSON(_ data: Data) -> CoinData? {
        let decoder = JSONDecoder()
        do {
            let decodedData = try decoder.decode(CoinData.self, from: data)
            return decodedData
            
        } catch {
            print(error)
            return nil
        }
    }
    
    

    
}
